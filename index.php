		<h2>Introduktion till webbutveckling</h2>
		<p>Denna webbplats innehåller material från en grundkurs i webbutveckling. De som läser <a href="http://edu.mah.se/sv/Course/DA156A#Syllabus">kursen</a> är den primära målgruppen, men mycket kan förhoppningsvis vara användbart även för andra. Allt material är publicerat under en <a href="http://creativecommons.org/licenses/by/4.0/deed.sv">fri licens</a>, vilket gör att det kan återanvändas av andra, så länge upphovsman anges.</p>

		<h3>Syfte</h3>
		<p>I den bakomliggande kursplanen beskrivs syfte och mål:</p>
		<div class="well">Kursen syftar till att introducera fundamentala koncept och tekniker som ingår i modern webbutveckling. Studenten ska ges praktiska färdigheter i utveckling av webbplatser, liksom en teoretisk bakgrund till de begränsningar och möjligheter som ges av aktuella standarder och plattformar. Studenten ska också utveckla en inledande förståelse av hur användarbehov och -beteende kan styra utvecklingen av ett gränssnitt.</div>
		<p>Den beskrivningen är väldigt avsiktligt formulerad utan att nämna specifika tekniker. Istället ligger fokus på "modern webbutveckling" och "aktuella standarder och plattformar". Målet är således att hålla en bra balans mellan grundläggande principer och viktiga nya utvecklingar. Hösten 2016 betyder det bland annat HTML5 och CSS 3, med fokus på tillgänglighet och användbarhet för olika enheter.</p>

		<h3>Upplägg</h3>
		<p>Webbplatsen är baserad på en högskolekurs där ämnen introduceras vid en serie föreläsningar. Bilder, länkar och exempel publiceras <a href="/lectures.php">här</a>.</p>
		<p>Kursen är också praktiskt - ämnet lär man sig genom själv producera och experimentera. Därför finns några laborationer, som kan ses som rekommenderade övningar. Studenter vid kursen har tillgång till handledning för dessa.</p>
		<p>Studenter examineras med inlämningsuppgifter och en tentamen. Instruktioner för uppgifterna publiceras också här.</p>


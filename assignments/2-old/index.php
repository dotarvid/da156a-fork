</p>

<p>Presentationen måste följa beskrivningen/figuren nedan, men inom dessa ramar är du fri att välja utseendet. I slutet av detta dokument återfinns en mer detaljerad figur, som tydliggör vissa aspekter.</p>

<h4>1.1 Innehåll på webbsidorna</h4>

<h5>1.1.2. Startsida</h5>

<p>Målet mer startsidan är att förklara varför webbplatsen existerar. En slumpmässig internetsurfare som hittar till din webbplats bör kunna förstå detta efter att ha läst startsidan.</p>

<p>Saker som kan vara bra att inkludera är alltså:</p>

<ul>
    <li>kort beskrivning av syftet med webbsida, kurs och program</li>
    <li>vem som står bakom</li>
    <li>kontaktuppgifter (e-postadress).</li>
</ul>

<h5>1.1.3. Interaktiv beskrivning av JavaScript</h5>

<p>Denna webbsida (som du gärna kan döpa till något roligare&#8230;) ska dels innehålla en löptext, och dels tre körbara JavaScript-exempel.</p>

<p>Syftet med texten är beskriva vissa intressanta koncept som relaterar till JavaScript. Inled med en kortare beskrivning om vad JavaScript är och vad det brukar användas till. (Du kanske redan har skrivit en sådan beskrivning?) Därefter ska texten beskriva tre av följande ämnen/koncept:</p>

<ul>
    <li>Välj en webbplats från nätet och beskriv hur JavaScript används där. (På en övergripande nivå, källkoden behöver inte studeras.)</li>
    <li>Vad är ett programmeringsspråk?</li>
    <li>Tillgänglighet och JavaScript, vad bör man tänka på? (<em>Graceful degredation</em>, <em>progressive enhancement</em>, <em>unobtrusive JavaScript</em>.)</li>
    <li>Vad är en algoritm?</li>
    <li>Vad är DOM, Document Object Model, och hur hänger det ihop med JavaScript?</li>
    <li>Vad är ett JavaScript-bibliotek (eller -ramverk) och varför används sådana?</li>
    <li>Två nya möjligheter med JavaScript och HTML5. Rita med <code>canvas</code>, och positionering med W3C Geolocation API. (Beskriv övergripande.)</li>
</ul>

<p>Välj de tre punkterna som intresserar dig mest och skriv minst 150 ord om var och en. För varje punkt ska du också inkludera minst en länk till en webbsida med ytterligare information.</p>

<p>Utöver texten ska denna webbsida också innehålla tre interaktiva exempel som visar några varianter av vad som kan göras med JavaScript. Exemplen ska aktiveras med vars en knapp, som alla ligger i ett separat område (se figur). Exemplen ska använda vissa JavaScript-funktioner:</p>

<ol>
    <li><strong>Visa poppuppfönster</strong>: Använd <code>alert()</code> för att visa ett meddelande.</li>
    <li><strong>Textinmatning</strong>: Använd <code>prompt()</code> - och visa inmatad text på valfritt vis (t. ex. med <code>alert()</code>).</li>
    <li><strong>Byt utseende</strong>: Ändra valfri aspekt av webbsidans utseende, t. ex. bakgrundsfärg. Detta kan göras genom att byta klass på det element som ska förändras.</li>
</ol>

<p>Du ansvarar för att skapa den JavaScript-kod som krävs för att exemplen fungerar.</p>

<h3>2. Kravspecifikation</h3>

<ul>
    <li>Webbplatsen måste följa beskrivningen ovan, avseende innehåll, visuell utformning och funktionalitet.</li>
    <li>Alla webbsidor ska validera enligt HTML5.</li>
    <li>Semantisk HTML ska användas. Detta betyder att taggar ska välja, så att de i så stor grad som möjligt beskriver innehåll och struktur.</li>
    <li>Alla webbsidor ska använda samma stilmall(ar).</li>
    <li>CSS-mall(ar) ska validera enligt CSS nivå 3.</li>
    <li>Huvuddelen (gärna all) av din JavaScript-kod ska läggas i en extern fil.</li>
</ul>


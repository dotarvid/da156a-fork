<h2>Inlämningsuppgift 2</h2>
<p>Uppgiften går ut på att redovisa dina kunskaper kring JavaScript, liksom din förmåga att skapa en CSS-baserad layout. Detta gör du genom att lämna in ett resultat som följer specifikationerna nedan. Inlämningsuppgiften är individuell, vilket betyder att studenten enskilt ska skriva sin inlämning och kunna redogöra för alla delar av den.</p>

<h3>1. Uppgiften</h3>

<p>Du ska skapa en webbplats bestående av minst 2 webbsidor, som båda följer samma visuella utformning.</p>

